public class Main {
    public static void main(String[] args)
    {
        int userInput;

        userInput = 8;

        if (userInput % 2 == 0)
        {
            System.out.println(userInput + " is an even number.");
        }
        else
        {
            System.out.println(userInput + " is an odd number.");
        }
    }
}